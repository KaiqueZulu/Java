public class booleans {
    public static void main(String[] args) {
        // Primary / primitive
        boolean True = true;
        boolean False = false;

        // Not primary / Not primitive (primary classes)
        Boolean T = true;
        Boolean F = false;

        System.out.println("True = " + True);
        System.out.println("False = " + False);
        System.out.println("T = " + T);
        System.out.println("F = " + F);

    }
}
