public class ifElse {
    public static void main(String[] args) throws Exception {
       int number = 5;
       if (number > 5) {
        System.out.println("Yes, the number " + number + " is greater than 5!");
       }else if(number == 5){
        System.out.println("The number " + number + " is equal to 5!");
       }else{
        System.out.println("No, the number " + number + " is not greater than 5!");
       };
    }
}
